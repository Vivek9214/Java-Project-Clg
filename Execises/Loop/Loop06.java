public class Loop6 {
    public static void main(String[] args) {
        //     6.Pattern
        int n = 1; 
        for(int i= n; i<=6; i++){
            int m= 1;
            for(int j =1; j<=i; j++){
                System.out.print(m);
                m++;
            }
            System.out.println();
        }
    }
}
